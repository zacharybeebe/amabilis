from fpdf import FPDF

##### PDF FORMAT MODULE FOR MAIN PROGRAM
class PDF(FPDF):
    
    def footer(self):
        # Position at 1.5 cm from bottom
        self.set_y(-15)
        # Arial italic 8
        self.set_font('Times', 'I', 8)
        # Page number
        self.cell(0, 10, 'Page ' + str(self.page_no()) + '/{nb}', 0, 0, 'C')











